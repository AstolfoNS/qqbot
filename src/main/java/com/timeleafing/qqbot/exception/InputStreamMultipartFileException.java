package com.timeleafing.qqbot.exception;

public class InputStreamMultipartFileException extends RuntimeException {

    public InputStreamMultipartFileException() {
        super();
    }

    public InputStreamMultipartFileException(String message) {
        super(message);
    }

    public InputStreamMultipartFileException(String message, Throwable cause) {
        super(message, cause);
    }

    public InputStreamMultipartFileException(Throwable cause) {
        super(cause);
    }
}
